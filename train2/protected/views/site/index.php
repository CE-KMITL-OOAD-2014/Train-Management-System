เนื่องจากทางรถไฟสายเหนือที่สร้างขึ้นเป็นทางขนาดกว้าง ๑.๔๓๕
เมตร อันเป็นขนาดมาตรฐานสากลที่ใช้กันเป็นส่วนใหญ่ในโลก (Standard Gauge)
ส่วนทางสายใต้เป็นทางขนาดกว้าง ๑ เมตร (Meter Gauge)
จึงก่อให้เกิดความไม่สะดวกในการจะเดินทางติดต่อร่วมกัน
ทำให้ต้องขนถ่ายสับเปลี่ยน แทนที่จะขนส่งได้ทอดเดียวถึง
ต่อมาได้มีการเปลี่ยนทางจากขนาดกว้าง ๑.๔๓๕ เมตร เป็น ๑ เมตร
เหมือนกันทั้งหมด แล้วให้สร้างทางแยกจากสถานีบางซื่อ
ข้ามแม่น้ำเจ้าพระยาที่สะพานพระราม ๖ ไปบรรจบกับทางสายใต้ที่สถานีตลิ่งชัน
การเปลี่ยนแปลงครั้งนี้
ทำให้การคมนาคมทางรถไฟสามารถเชื่อมโยงติดต่อกันได้โดยสะดวก
ทั่วราชอาณาจักร รวมทั้งให้ความสะดวก
ในการเดินรถติดต่อกับการรถไฟของประเทศใกล้เคียง เช่น การรถไฟมลายา
การรถไฟสิงคโปร์ การรถไฟกัมพูชา ซึ่งมีขนาดของทางกว้าง ๑ เมตร
